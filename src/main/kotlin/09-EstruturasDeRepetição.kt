fun main(){


    println("*** Estruturas de Repetição ***")

    //While Enquanto//
    var listaCompras = arrayOf("Arroz","Feijão","Batata","Uva","Banana","Carne","Leite","Ovos","Frango")

    var indice = 0

    //estrutura de repeticao (enquanto) (Condiçao do While) Enquanto for verdadeiro sera executado ,se tornar falso
    //para de ser executado//
    while (indice <=listaCompras.size -1){
        println(listaCompras[indice])
        indice++

    }

}